void main(List<String> arguments) {
  /*
  bool is a data type
  isOn is a variable - something that will change
  */
  bool isOn; //this is a null object
  //you can also declare a initial value ex: bool isOn = true or var isOn = true
  print('isOn = ${isOn}');

  isOn = true; 
  print('isOn = ${isOn}');

  isOn = false; 
  print('isOn = ${isOn}');


}
